package day10_controlflowstatements;

public class forLoop {

	public static void main(String[] args) {
		
		for(int i =0 ; i<5 ; i++  ) {
			System.out.println("Hello");
				
		}
		
		// i  condition  action        increment(i++) i=i+1
		// 0   true       Print hello    2
        //....                           3
		
		
		
		
	}
	
	

}
